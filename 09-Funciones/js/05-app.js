// Parámetros y argumentos
function sumar(a,b) { // (parametroA, parámetroB)
    console.log(a+b);
}
sumar(2,3); // Argumentos (argumentoA, argumentoB).
sumar(220,189);

function saludar(nombre, apellido) {
    console.log(`Hola ${nombre} ${apellido}, bienvenido otra vez.`);
}
saludar('Felipe', 'González');