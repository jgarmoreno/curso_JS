// Pasar parámetros a arrow functions
// 1 parámetro
const aprendiendo = tecnologia => `Aprendiendo ${tecnologia}`; // Tampoco es necesario los paréntesis en 1 línea
console.log(aprendiendo('Javascript'));

// 2 parámetros
const aprendiendo2 = (tecnologia, tecnologia2) => `Aprendiendo ${tecnologia} y ${tecnologia2}`;
console.log(aprendiendo2('JavaScript', 'Node.js'));
