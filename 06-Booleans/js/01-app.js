// Booleans
const boolean1 = true;
const boolean2 = false;

console.log(boolean1); // true
console.log(boolean2); // false

console.log(typeof boolean1); // boolean
console.log(typeof boolean2); // boolean

const boolean4 = new Boolean(true); // Este tipo de constructor suele crear un objeto, no del tipo primitivo que seria boolean.
console.log(boolean4); // true
console.log(typeof boolean4); // object

